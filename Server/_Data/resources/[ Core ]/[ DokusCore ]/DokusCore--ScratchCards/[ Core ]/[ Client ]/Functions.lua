function AddItem(Reward)
  local Tab = {}
  local Ins = 'DokusCore:Core:DBIns:Inventory'
  local Set = 'DokusCore:Core:DBSet:Inventory'
  local Inv = TSC('DokusCore:Core:DBGet:Inventory', { 'User', 'Item', { SteamID, CharID, 'scratchprize' } })

  if (Inv.Exist) then
    local Dec = Decoded(Inv.Result[1].Meta)[1]
    local Prize = Round((Dec.Reward + Reward), 2)
    Tabi(Tab, { Reward = Prize })
    local Meta = Encoded(Tab)
    TriggerServerEvent(Set, { 'User', 'AddItem', { SteamID, CharID, 'scratchprize', 1, Inv.Result[1].Amount, Meta } })
  else
    Tabi(Tab, { Reward = Round(Reward, 2) })
    local Meta = Encoded(Tab)
    TriggerServerEvent(Ins, { 'User', 'InsertItem', { SteamID, CharID, 'Consumable', 'scratchprize', 1, Meta } })
  end
end
