RegisterServerEvent('DokuCore:ScratchCards:Set:JackPot')
AddEventHandler('DokuCore:ScratchCards:Set:JackPot', function(Data, Value)
  if (not (Data[1].Exist)) then
    TriggerEvent('DokusCore:Core:DBIns:Storages', { 'JackPot', { 0 } })
  else
    if (Value ~= nil) then
      TriggerEvent('DokusCore:Core:DBIns:Storages', { 'JackPot', { tonumber(Value) } })
    end
  end
end)
