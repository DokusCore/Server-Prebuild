RegisterServerEvent('DokusCore:Characters:SetSource')
AddEventHandler('DokusCore:Characters:SetSource', function(SteamID, Name)
  local _source = source
  local Index = { SteamID = SteamID, Name = Name, Source = source }
  TriggerEvent('DokusCore:Sync:Set:UserData', { 'insertonline', Index })
end)
