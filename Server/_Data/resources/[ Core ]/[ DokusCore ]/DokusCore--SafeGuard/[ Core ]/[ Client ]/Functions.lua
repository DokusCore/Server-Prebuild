function FrameReady()
  local Data = TCTCC('DokuCore:Sync:Get:CoreData')
  return Data.FrameReady
end
--------------------------------------------------------------------------------
--------------------------------------------------------------------------------
function UserInGame()
  local Data = TCTCC('DokusCore:Sync:Get:UserData')
  return Data.UserInGame
end
--------------------------------------------------------------------------------
--------------------------------------------------------------------------------
function CheckUserAlive()
  while not (Alive) do
    local Data = TCTCC('DokusCore:Sync:Get:UserData')
    if (Data.Alive) then Alive = true end Wait(2000)
  end
end
--------------------------------------------------------------------------------
--------------------------------------------------------------------------------
function TakeAction(SteamID, Reason, IP, License, XBox, MLive)
  TriggerServerEvent('DokusCore:SafeGuard:CheckPos:TakeAction', SteamID, Reason, IP, License, XBox, MLive)
end
--------------------------------------------------------------------------------
--------------------------------------------------------------------------------
function IsError(Reason)
  if (Reason == '') then Notify('', 'TopRight', 10000) end
end
