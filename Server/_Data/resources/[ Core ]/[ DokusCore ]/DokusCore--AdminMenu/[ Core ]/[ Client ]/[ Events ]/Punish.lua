RegisterNUICallback('slap', function(Data)
  Data.staff = GetPlayerName(PlayerId())
  TriggerServerEvent('DokusCore:AdminMenu:PassToPlayer', {
    Action = 'slap',
    Source = TN(Data.player),
    Name   = Data.staff,
    tSource = Data.MySource,
  })
end)
