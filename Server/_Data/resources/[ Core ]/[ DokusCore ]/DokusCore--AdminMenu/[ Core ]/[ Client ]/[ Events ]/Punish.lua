RegisterNUICallback('warn', function(Data)
    Data.staff = GetPlayerName(PlayerId())
    if (Data.player == GetPlayerServerId(PlayerId())) then
      NoteObjective(("Warning from " .. Data.staff), Data.data, 'Horn', 15000)
    end
end)
