RegisterNetEvent('DokusCore:AdminMenu:PassToPlayer', function(Data)
  if (Low(Data.Action) == 'teleporttoadmin') then
    SetEntityCoords(PedID(), Data.Coords)
    NoteObjective(("Teleported! by " .. Data.Name), "You've got teleported by an admin", 'Horn', 5000)
  elseif (Low(Data.Action) == 'slap') then
    SendNUIMessage({ type = "gif", action = "slap" })
    NoteObjective(("You got slapped! by " .. Data.Name), 'Bad bad you!', 'Horn', 10000)
  elseif (Low(Data.Action) == 'warn') then
    SendNUIMessage({ type = "gif", action = "warning" })
    NoteObjective(("Warning from " .. Data.Name), Data.MSG, 'Horn', 60000)
  elseif (Low(Data.Action) == 'dm') then
    NoteObjective(("DM from " .. Data.Name), Data.MSG, 'Horn', 15000)
  elseif (Low(Data.Action) == 'kick') then
    CreateLog('AdminMenu', 'Kicked', Data.Reason)
    if (Data.MySource == Data.Source) then NoteObjective("System", "You can't kick yourself!", 'Horn', 5000) return end
    TriggerServerEvent('DokusCore:Core:KickPlayer', { Data.Source, Data.Reason })
  elseif (Low(Data.Action) == 'tpmtouser1') then
    local Coords = GetEntityCoords(PedID())
    TriggerServerEvent('DokusCore:AdminMenu:PassToPlayer', {
      Action = 'TPMToUser2',
      Source = Data.MySource,
      Name   = Data.staff,
      Coords = Coords,
    })
    NoteObjective((Data.Name.." Teleported!"), "A admin has teleported to your location", 'Horn', 5000)
  elseif (Low(Data.Action) == 'tpmtouser2') then
    SetEntityCoords(PedID(), Data.Coords)
  end

  -- New:
  if (Low(Data.Action) == 'getspeccoords') then
    local Coords = GetCoords(PedID())
    TriggerServerEvent('DokusCore:AdminMenu:PassToPlayer', {
      Action = 'Spectate',
      Source = Data.MySource,
      Name   = Data.staff,
      Coords = Coords,
      MySource = Data.Source
    })
  elseif (Low(Data.Action) == 'spectate') then
    SetEntityCoords(PedID(), Data.Coords[1], Data.Coords[2], (Data.Coords[3] + 2.0))
    TriggerEvent('DokusCore:Admin:Spectate') Wait(2000)
  end
end)
