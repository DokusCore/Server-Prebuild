function TableItems(Data)
  local Table = {}
  for k,v in pairs(Data.Result) do
    table.insert(Table, {
      Item     = v.Item,
      Name     = v.Name,
      Type     = v.Type,
      CTI      = v.CTI,
      Limit    = v.InvLimit,
      Meta     = v.Metabolism,
      CanPlace = v.CanPlace,
      Hunger   = v.Hunger,
      Thirst   = v.Thirst,
      Health   = v.Health,
      Stamina  = v.Stamina,
      GHI      = v.GHI,
      GHO      = v.GHO,
      GSI      = v.GSI,
      GSO      = v.GSO,
      Prop     = v.Prop,
      Ani      = v.Ani,
      Event    = v.Event
    })
  end return Table
end
