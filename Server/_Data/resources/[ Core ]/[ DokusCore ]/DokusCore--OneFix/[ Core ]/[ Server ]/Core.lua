local DeLog = _OneFix.Debug.Boats.Log
--------------------------------------------------------------------------------
--------------------------------------------------------------------------------
CreateThread(function()
  if (_Modules.OneFix) then
    AddEventHandler('entityCreating', function(E)
      local Type = GetEntityType(E)
      local Pop  = GetEntityPopulationType(E)
      if ((Type == 2) and (Type == 'boat')) then
        if ((Pop ~= 7) and (Pop ~= 8)) then
          if (DeLog) then print("Prevented Boat from spawning", Type, Pop) end
          CancelEvent()
        end
      end
    end)
  end
end)
