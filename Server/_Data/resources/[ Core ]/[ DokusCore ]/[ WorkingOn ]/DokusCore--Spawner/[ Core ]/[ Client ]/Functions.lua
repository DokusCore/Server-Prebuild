function WalkUserToTailor()
  IsWalking = true
  for k,v in pairs(Tailor) do
    if (Low(v.ID) == Low(SpawnPos.ID)) then
      while IsWalking do Wait(100)
        local Dist = GetDistance(v.Coords)
        if (Dist < 0.5) then IsWalking = false return end
      end
    end
  end
end
