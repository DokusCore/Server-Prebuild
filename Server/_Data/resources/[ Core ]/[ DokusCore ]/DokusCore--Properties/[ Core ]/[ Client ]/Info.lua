RegisterNetEvent('DokusCore:Properties:Show:DoorToggle', function(Type)
  local Show, Count = true, 0
  while (Show) do Wait(1)
    Count = (Count + 1)
    if (Count >= 100) then Show, Count = false, 0 end
    for k,v in pairs(HouseDoors) do
      local Dec = Decoded(v.Coords)
      local Vec = vec3(Dec.x, Dec.y, Dec.z)
      local Dist = GetDistance(Vec)
      if (Dist <= 3.0) then
        for i,p in pairs(Doors) do
          if (Floor(p.Hash) == Floor(v.Hash)) then
            local oX, oY, oZ = p.Offset[1], p.Offset[2], p.Offset[3]
            local V1, V2, V3 = ((Vec.x - 0.30) + oX), ((Vec.y + 0.50) + oY), ((Vec.z + 1.0) + oZ)
            if (Type == 0) then
              DrawText3DHousing('~COLOR_BLUE~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~', V1, V2, V3)
              DrawText3DHousing('Door ~COLOR_GREEN~Opened', V1, V2, (V3 - 0.05))
              DrawText3DHousing('~COLOR_BLUE~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~', V1, V2, (V3 - 0.10))
            elseif (Type == 1) then
              DrawText3DHousing('~COLOR_BLUE~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~', V1, V2, V3)
              DrawText3DHousing('Door ~COLOR_RED~Closed', V1, V2, (V3 - 0.05))
              DrawText3DHousing('~COLOR_BLUE~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~', V1, V2, (V3 - 0.10))
            end
          end
        end
      end
    end
  end
end)
