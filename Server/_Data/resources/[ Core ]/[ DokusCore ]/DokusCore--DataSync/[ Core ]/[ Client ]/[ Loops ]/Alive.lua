-- This checks the users death status every x milliseconds.
-- If the user dies or logs out, then his or her status will be updated.
-- Then all plugins that need this information with be fed with the new
-- player death status.
--------------------------------------------------------------------------------
--------------------------------------------------------------------------------
local LastCheck = false
--------------------------------------------------------------------------------
--------------------------------------------------------------------------------
local function SetUserAlive()
  TriggerEvent('DokusCore:SafeGuard:Set:UserAlive', { LastCheck })
end
--------------------------------------------------------------------------------
--------------------------------------------------------------------------------
local function SetUserDead()
  TriggerEvent('DokusCore:SafeGuard:Set:UserAlive', { LastCheck })
end
--------------------------------------------------------------------------------
--------------------------------------------------------------------------------
local function UUAS()
  LastCheck = not LastCheck
  if (_User.Alive) then SetUserAlive() end
  if not (_User.Alive) then SetUserDead() end
end
--------------------------------------------------------------------------------
--------------------------------------------------------------------------------
CreateThread(function()
  while true do Wait(500) if (LastCheck ~= _User.Alive) then UUAS() end end
end)
