-- Update players city location in Datasync
--------------------------------------------------------------------------------
CreateThread(function()
  while (not _Core.FrameReady) do Wait(1000) end
  while (not _User.UserInGame) do Wait(1000) end
  while true do Wait(1)
    local Coords = GetCoords(PedID())
    local LocHash = Citizen.InvokeNative(0x43AD8FC02B429D33, Coords, 1)
    local MyLoc = nil

    for k,v in pairs(Data_Cities) do
      if (TN(k) == TN(LocHash)) then MyLoc = TS(v) end
    end

    _User.Area = TS(MyLoc)
    Wait(10000)
  end
end)
