CreateThread(function()
  while true do Wait(0)
    local Control = IsControlJustReleased(0, _Keys['TAB'])
    if (Control) then
      local MN, MT, MO = nil, nil, nil
      for k,v in pairs(_DokusMenus.MainMenu) do MN, MT, MO = v.MenuName, v.MenuTitle, v.MenuOptions end
      OpenMenu()
      SendNUIMessage({ MenuName = MN, MenuTitle = MT, MenuOptions = MO })
    end
  end
end)
